private val DarkColorScheme = darkColorScheme(
    primary = Purple80,
    secondary = PurpleGrey80,
    tertiary = Pink80
)

private val LightColorScheme = lightColorScheme(
    primary = PrimaryColor,
    secondary = PurpleGrey40,
    tertiary = Pink40,
    background = Color.White,
    surface = Color.White,

    /* Other default colors to override
    background = Color(0xFFFFFBFE),
    surface = Color(0xFFFFFBFE),
    onPrimary = Color.White,
    onSecondary = Color.White,
    onTertiary = Color.White,
    onBackground = Color(0xFF1C1B1F),
    onSurface = Color(0xFF1C1B1F),
    */
)

@Composable
fun YogaYoTheme(
    // darkTheme 파라미터 제거
    // dynamicColor: Boolean = true, // 필요하다면 이 줄도 제거
    content: @Composable () -> Unit
) {
    val colorScheme = LightColorScheme // 항상 라이트 모드 색상 사용
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = Color.White.toArgb()

            // 상태바 아이콘 색상 (흰색 배경에는 어두운 아이콘)
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = true
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}
val SplashFontFamily = FontFamily(
    Font(R.font.gumi_romance, FontWeight.Normal), // 필요 시 일반 굵기 폰트
)